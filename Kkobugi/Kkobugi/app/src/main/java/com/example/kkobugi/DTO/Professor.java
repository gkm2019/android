package com.example.kkobugi.DTO;

public class Professor {

    private int professorIdx;
    private String professorName;
    private String departmentName;
    private String professorField;
    private String professorTell;
    private String professorAddr;
    private String professorEmail;
    private String professorImage;

    public int getProfessorIdx() {
        return professorIdx;
    }

    public void setProfessorIdx(int professorIdx) {
        this.professorIdx = professorIdx;
    }

    public String getProfessorName() {
        return professorName;
    }

    public void setProfessorName(String professorName) {
        this.professorName = professorName;
    }

    public String getDepartmentName() {
        return departmentName;
    }

    public void setDepartmentName(String departmentName) {
        this.departmentName = departmentName;
    }

    public String getProfessorField() {
        return professorField;
    }

    public void setProfessorField(String professorField) {
        this.professorField = professorField;
    }

    public String getProfessorTell() {
        return professorTell;
    }

    public void setProfessorTell(String professorTell) {
        this.professorTell = professorTell;
    }

    public String getProfessorAddr() {
        return professorAddr;
    }

    public void setProfessorAddr(String professorAddr) {
        this.professorAddr = professorAddr;
    }

    public String getProfessorEmail() {
        return professorEmail;
    }

    public void setProfessorEmail(String professorEmail) {
        this.professorEmail = professorEmail;
    }

    public String getProfessorImage() {
        return professorImage;
    }

    public void setProfessorImage(String professorImage) {
        this.professorImage = professorImage;
    }
}
